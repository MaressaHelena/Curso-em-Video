# Desenvolva um programa que leia as duas notas de um aluno, calcule e mostre a sua média

nota1 = float(input('Entre com a nota 1: '))
nota2 = float(input('Entre com a nota 2: '))
media = (nota1+nota2)/2
print(f'A sua média é: {media}!')
print('FIM')